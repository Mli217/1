def save_memories_to_file(memories):
    data = {
        "memories": memories,
        "last_save_time": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    }
    with open("memories_config.json", "w", encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def load_memories_from_file():
    try:
        with open("memories_config.json", "r", encoding='utf-8') as f:
            data = json.load(f)
        return data.get("memories", {})
    except:
        return {}
